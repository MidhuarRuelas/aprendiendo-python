# Diferentes tipos de datos
nombre = "Midhuar"              # String (texto)
edad = 40                       # Integer (entero)
altura = 1.75                   # Float (decimal)
es_programador = True           # Boolean (verdadero/falso)

# Mostrar cada tipo
print("=== MIS DATOS ===")
print("Nombre:", nombre)
print("Edad:", edad)
print("Altura:", altura, "metros")
print("¿Es programador?:", es_programador)

# Operaciones con números
print("\n=== OPERACIONES ===")
print("En 5 años tendré:", edad + 5, "años")
print("Mi altura en cm:", altura * 100)